class Game(object):
    """Retriever object gets data from Rottentomatoes.com and updates game stats"""

    def __init__(self):
        """Retriever object is initialized""" 

        self.connections = {}
        self.chain = []
        self.score = len(self.chain)

        self.strikes = 0

        self.current = None
        self.current_list = None
